#!/usr/bin/env python3
"""
Receive Hololink/LinuxDataPlane UDP payloads and reconstruct raw frame bytes.

This script listens on a UDP port, parses the custom IB-like payload generated by
linux_transmitter.cpp, and rebuilds frames when the WRITE_IMMEDIATE metadata
packet is received.
"""

from __future__ import annotations

import argparse
import socket
import struct
import sys
import time
import zlib
from dataclasses import dataclass
from pathlib import Path
from typing import Dict, List, Optional, Tuple


IB_OPCODE_WRITE = 0x2A
IB_OPCODE_WRITE_IMMEDIATE = 0x2B

BT_HEADER_SIZE = 12
RET_HEADER_SIZE = 16
IB_HEADER_SIZE = BT_HEADER_SIZE + RET_HEADER_SIZE
CRC_SIZE = 4

FRAME_METADATA_SIZE = 128
IMMEDIATE_SIZE = 4
METADATA_PAYLOAD_SIZE = IMMEDIATE_SIZE + FRAME_METADATA_SIZE

PAGE_SIZE = 128


@dataclass
class DataPacket:
    psn: int
    vaddr: int
    payload: bytes
    src_ip: str
    src_port: int
    rx_ts: float


@dataclass
class FrameMetadata:
    flags: int
    psn: int
    crc: int
    timestamp_s: int
    timestamp_ns: int
    bytes_written: int
    frame_number: int
    metadata_s: int
    metadata_ns: int


def parse_ib_udp_payload(datagram: bytes) -> Optional[Tuple[int, int, int, int, bytes]]:
    """
    Parse one UDP payload packet emitted by LinuxTransmitter.
    Returns (opcode, psn, vaddr, content_size, content_bytes) or None.
    """
    if len(datagram) < IB_HEADER_SIZE + CRC_SIZE:
        return None

    # BT header: opcode(1), flags(1), p_key(2), qp(4), psn(4)
    opcode, _flags, _p_key, _qp, psn = struct.unpack_from(">BBHII", datagram, 0)
    # RET header: vaddress(8), rkey(4), content_size(4)
    vaddr, _rkey, content_size = struct.unpack_from(">QII", datagram, BT_HEADER_SIZE)

    total_len = IB_HEADER_SIZE + content_size + CRC_SIZE
    if len(datagram) < total_len:
        return None

    content = datagram[IB_HEADER_SIZE : IB_HEADER_SIZE + content_size]
    return opcode, psn & 0xFFFFFF, vaddr, content_size, content


def parse_frame_metadata(raw: bytes) -> FrameMetadata:
    """
    Parse FrameMetadata structure serialized via serialize_frame_metadata().
    """
    if len(raw) < FRAME_METADATA_SIZE:
        raise ValueError("metadata payload too short")

    flags, psn, crc = struct.unpack_from(">III", raw, 0)
    timestamp_s = struct.unpack_from(">Q", raw, 12)[0]
    timestamp_ns = struct.unpack_from(">I", raw, 20)[0]
    bytes_written = struct.unpack_from(">Q", raw, 24)[0]
    frame_number = struct.unpack_from(">I", raw, 32)[0]
    metadata_s = struct.unpack_from(">Q", raw, 36)[0]
    metadata_ns = struct.unpack_from(">I", raw, 44)[0]

    return FrameMetadata(
        flags=flags,
        psn=psn & 0xFFFFFF,
        crc=crc,
        timestamp_s=timestamp_s,
        timestamp_ns=timestamp_ns,
        bytes_written=bytes_written,
        frame_number=frame_number,
        metadata_s=metadata_s,
        metadata_ns=metadata_ns,
    )


def align_up(value: int, alignment: int) -> int:
    return ((value + alignment - 1) // alignment) * alignment


class FrameReassembler:
    def __init__(self) -> None:
        self.pending_packets: List[DataPacket] = []
        self.completed_frames = 0

    def add_data_packet(self, packet: DataPacket) -> None:
        self.pending_packets.append(packet)

    def complete_frame_from_metadata(
        self, metadata_vaddr: int, metadata_content: bytes
    ) -> Optional[Tuple[FrameMetadata, int, bytes]]:
        """
        Reconstruct frame bytes from pending data packets using metadata packet info.
        Returns (frame_metadata, page, frame_bytes) or None if incomplete.
        """
        if len(metadata_content) < METADATA_PAYLOAD_SIZE:
            return None

        immediate_value = struct.unpack_from(">I", metadata_content, 0)[0]
        page = immediate_value & 0xFF
        frame_md = parse_frame_metadata(metadata_content[IMMEDIATE_SIZE : IMMEDIATE_SIZE + FRAME_METADATA_SIZE])

        frame_size = int(frame_md.bytes_written)
        if frame_size <= 0:
            return None

        metadata_offset = align_up(frame_size, PAGE_SIZE)
        frame_base_vaddr = metadata_vaddr - metadata_offset
        frame_end_vaddr = frame_base_vaddr + frame_size

        frame = bytearray(frame_size)
        coverage = bytearray(frame_size)
        used_indices: List[int] = []

        for i, pkt in enumerate(self.pending_packets):
            pkt_start = pkt.vaddr
            pkt_end = pkt.vaddr + len(pkt.payload)
            if pkt_end <= frame_base_vaddr or pkt_start >= frame_end_vaddr:
                continue

            dst_start = max(pkt_start, frame_base_vaddr) - frame_base_vaddr
            dst_end = min(pkt_end, frame_end_vaddr) - frame_base_vaddr
            src_start = max(frame_base_vaddr, pkt_start) - pkt_start
            copy_len = dst_end - dst_start
            if copy_len <= 0:
                continue

            frame[dst_start:dst_end] = pkt.payload[src_start : src_start + copy_len]
            coverage[dst_start:dst_end] = b"\x01" * copy_len
            used_indices.append(i)

        # Remove used packets from pending buffer.
        for i in reversed(used_indices):
            del self.pending_packets[i]

        if 0 in coverage:
            return None

        self.completed_frames += 1
        return frame_md, page, bytes(frame)


def run(args: argparse.Namespace) -> int:
    out_dir = Path(args.out_dir) if args.out_dir else None
    if out_dir:
        out_dir.mkdir(parents=True, exist_ok=True)

    sock = socket.socket(socket.AF_INET, socket.SOCK_DGRAM)
    sock.setsockopt(socket.SOL_SOCKET, socket.SO_RCVBUF, args.recv_buffer)
    sock.bind((args.listen_ip, args.listen_port))
    sock.settimeout(1.0)

    print(f"Listening on {args.listen_ip}:{args.listen_port}")
    if args.source_ip:
        print(f"Filtering source IP: {args.source_ip}")
    if args.source_port:
        print(f"Filtering source port: {args.source_port}")

    reassembler = FrameReassembler()
    packet_count = 0
    last_print = time.time()

    try:
        while True:
            try:
                data, (src_ip, src_port) = sock.recvfrom(65535)
            except socket.timeout:
                now = time.time()
                if now - last_print > 2.0 and not args.quiet:
                    print(f"Packets={packet_count} pending_chunks={len(reassembler.pending_packets)} frames={reassembler.completed_frames}")
                    last_print = now
                continue

            if args.source_ip and src_ip != args.source_ip:
                continue
            if args.source_port and src_port != args.source_port:
                continue

            parsed = parse_ib_udp_payload(data)
            if parsed is None:
                continue

            packet_count += 1
            opcode, psn, vaddr, _content_size, content = parsed

            if opcode == IB_OPCODE_WRITE:
                reassembler.add_data_packet(
                    DataPacket(
                        psn=psn,
                        vaddr=vaddr,
                        payload=content,
                        src_ip=src_ip,
                        src_port=src_port,
                        rx_ts=time.time(),
                    )
                )
                continue

            if opcode != IB_OPCODE_WRITE_IMMEDIATE:
                continue

            result = reassembler.complete_frame_from_metadata(vaddr, content)
            if result is None:
                if not args.quiet:
                    print(f"Metadata packet psn={psn} arrived, but frame is incomplete.")
                continue

            frame_md, page, frame_bytes = result
            crc32 = zlib.crc32(frame_bytes) & 0xFFFFFFFF
            print(
                f"frame={frame_md.frame_number} bytes={len(frame_bytes)} "
                f"psn={frame_md.psn} page={page} crc32=0x{crc32:08x}"
            )

            if out_dir:
                frame_path = out_dir / f"frame_{frame_md.frame_number:06d}.raw"
                frame_path.write_bytes(frame_bytes)

            if args.max_frames > 0 and reassembler.completed_frames >= args.max_frames:
                print("Reached max-frames, exiting.")
                return 0
    except KeyboardInterrupt:
        return 0
    finally:
        sock.close()


def build_arg_parser() -> argparse.ArgumentParser:
    p = argparse.ArgumentParser(description="Reassemble Hololink LinuxDataPlane UDP payloads into raw frame files.")
    p.add_argument("--listen-ip", default="0.0.0.0", help="Local IP to bind.")
    p.add_argument("--listen-port", type=int, required=True, help="Destination UDP port used by emulator stream.")
    p.add_argument("--source-ip", default="", help="Optional source IP filter.")
    p.add_argument("--source-port", type=int, default=0, help="Optional source port filter.")
    p.add_argument("--out-dir", default="", help="Optional output directory for frame_XXXXXX.raw dumps.")
    p.add_argument("--max-frames", type=int, default=0, help="Stop after N reconstructed frames (0 = run forever).")
    p.add_argument("--recv-buffer", type=int, default=8 * 1024 * 1024, help="SO_RCVBUF bytes.")
    p.add_argument("--quiet", action="store_true", help="Reduce periodic logs.")
    return p


def main() -> int:
    args = build_arg_parser().parse_args()
    return run(args)


if __name__ == "__main__":
    sys.exit(main())

